package xulytiengnoi;

import java.io.Serializable;

public class Country implements Serializable {
    
    protected String Ten;
    protected String Thudo;
    protected String Vitri;
    protected String Dientich;
    protected String Danso;
    protected String Quocki;
    

    public String getTen() {
        return Ten;
    }
    public void setTen(String Ten) {
        this.Ten = Ten;
    }


    public String getThudo() {
        return Thudo;
    }

    public void setThudo(String Thudo) {
        this.Thudo = Thudo;
    }

    public String getVitri() {
        return Vitri;
    }

    public void setVitri(String Vitri) {
        this.Vitri = Vitri;
    }

    public String getDientich() {
        return Dientich;
    }

    public void setDientich(String Dientich) {
        this.Dientich = Dientich;
    }

    public String getDanso() {
        return Danso;
    }

    public void setDanso(String Danso) {
        this.Danso = Danso;
    }

    public String getQuocki() {
        return Quocki;
    }

    public void setQuocki(String Quocki) {
        this.Quocki = Quocki;
    }

    
}
   